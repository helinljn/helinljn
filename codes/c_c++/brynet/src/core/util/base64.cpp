std::string base64_encode(std::string_view data)
{
    std::string ret;
    if (data.empty())
        return ret;

    // 预分配足够的空间，避免多次内存重新分配
    ret.reserve((data.size() + 2) / 3 * 4);

    const unsigned char* bytes_to_encode = reinterpret_cast<const unsigned char*>(data.data());
    size_t               datalen         = data.size();
    int i = 0;
    int j = 0;
    unsigned char char_array_3[3];
    unsigned char char_array_4[4];

    while (datalen--)
    {
        char_array_3[i++] = *(bytes_to_encode++);
        if (i == 3)
        {
            char_array_4[0] = (char_array_3[0] & 0xfc) >> 2;
            char_array_4[1] = ((char_array_3[0] & 0x03) << 4) + ((char_array_3[1] & 0xf0) >> 4);
            char_array_4[2] = ((char_array_3[1] & 0x0f) << 2) + ((char_array_3[2] & 0xc0) >> 6);
            char_array_4[3] = char_array_3[2] & 0x3f;

            for (i = 0; i < 4; i++)
                ret += base64::g_base64_characters[char_array_4[i]];
            i = 0;
        }
    }

    if (i)
    {
        for (j = i; j < 3; j++)
            char_array_3[j] = '\0';

        char_array_4[0] = (char_array_3[0] & 0xfc) >> 2;
        char_array_4[1] = ((char_array_3[0] & 0x03) << 4) + ((char_array_3[1] & 0xf0) >> 4);
        char_array_4[2] = ((char_array_3[1] & 0x0f) << 2) + ((char_array_3[2] & 0xc0) >> 6);
        char_array_4[3] = char_array_3[2] & 0x3f;

        for (j = 0; j < i + 1; j++)
            ret += base64::g_base64_characters[char_array_4[j]];

        while ((i++ < 3))
            ret += '=';
    }

    return ret;
}

std::string base64_decode(std::string_view data)
{
    std::string ret;
    if (data.empty())
        return ret;

    // 预分配足够的空间，避免多次内存重新分配
    ret.reserve(data.size() * 3 / 4);

    const char* bytes_to_decode = data.data();
    size_t      datalen         = data.size();
    int i   = 0;
    int j   = 0;
    int in_ = 0;
    unsigned char char_array_3[3];
    unsigned char char_array_4[4];

    while (datalen-- && (bytes_to_decode[in_] != '=') && base64::is_base64_character_internal(bytes_to_decode[in_]))
    {
        char_array_4[i++] = bytes_to_decode[in_];
        in_++;

        if (i == 4)
        {
            for (i = 0; i < 4; i++)
                char_array_4[i] = base64::g_base64_decode_table[static_cast<unsigned char>(char_array_4[i])];

            char_array_3[0] = (char_array_4[0] << 2) + ((char_array_4[1] & 0x30) >> 4);
            char_array_3[1] = ((char_array_4[1] & 0xf) << 4) + ((char_array_4[2] & 0x3c) >> 2);
            char_array_3[2] = ((char_array_4[2] & 0x3) << 6) + char_array_4[3];

            for (i = 0; i < 3; i++)
                ret += char_array_3[i];
            i = 0;
        }
    }

    if (i)
    {
        for (j = i; j < 4; j++)
            char_array_4[j] = 0;

        for (j = 0; j < 4; j++)
            char_array_4[j] = base64::g_base64_decode_table[static_cast<unsigned char>(char_array_4[j])];

        char_array_3[0] = (char_array_4[0] << 2) + ((char_array_4[1] & 0x30) >> 4);
        char_array_3[1] = ((char_array_4[1] & 0xf) << 4) + ((char_array_4[2] & 0x3c) >> 2);
        char_array_3[2] = ((char_array_4[2] & 0x3) << 6) + char_array_4[3];

        for (j = 0; j < i - 1; j++)
            ret += char_array_3[j];
    }

    return ret;
}

