# =============================================================================
# 1.1 Python 是什么？
# =============================================================================
#
# Python 是一种高级、解释型、动态类型的通用编程语言。
# 由 Guido van Rossum 于 1991 年创建，目前最常用的是 Python 3 系列。
#
# ── 与 C/C++ 的核心差异对比 ──────────────────────────────────────────────────
#
# 特性                C/C++                       Python
# ─────────────────────────────────────────────────────────────────────
# 类型系统            静态类型（编译时确定）          动态类型（运行时确定）
# 内存管理            手动（malloc/free/new/delete）  自动（垃圾回收器 GC）
# 执行方式            编译为机器码（速度快）           解释执行（速度较慢）
# 代码块分隔          大括号 { }                      缩进（4个空格）
# 变量声明            必须声明类型                    直接赋值即可
# 字符串              char 数组或 std::string         内置 str 类型（全功能）
# 容器                vector, map, set...             list, dict, set（内置）
# 异常处理            try-catch                       try-except
# 面向对象            class（头文件+源文件）           class（单文件即可）
# 标准库              STL                             极其丰富的标准库
# 代码量              较多（需要声明、类型等）          更少（语法简洁）
#
# ── Python 的主要应用场景 ─────────────────────────────────────────────────────
#
# - 数据科学 / 机器学习（NumPy, Pandas, TensorFlow）
# - Web 后端开发（Django, Flask, FastAPI）
# - 自动化脚本 / 运维工具
# - 网络爬虫
# - 科学计算
# - 系统工具开发
# - API 集成与数据处理
#
# ── Python 的设计哲学（The Zen of Python）────────────────────────────────────
#
# 在 Python 解释器中输入 import this 可以看到：
# - 美丽优于丑陋（Beautiful is better than ugly）
# - 明确优于隐晦（Explicit is better than implicit）
# - 简单优于复杂（Simple is better than complex）
# - 可读性很重要（Readability counts）
#


