# 1.8 __name__ == '__main__' 的含义
# =============================================================================
#
# 这是 Python 中非常常见的写法，相当于 C/C++ 的 main() 函数入口
#
# 当直接运行这个文件时：__name__ 的值是 '__main__'
# 当这个文件被其他文件导入时：__name__ 的值是文件名（模块名）
#
# 这样做的好处是：
# - 直接运行时，main() 函数会被调用（用于测试、演示）
# - 被导入时，main() 不会自动执行（只提供函数供别人用）
#
# 这与 C/C++ 不同：C/C++ 的 main() 是由操作系统调用的程序入口
# Python 没有强制的入口函数，只是按照惯例使用这个模式

if __name__ == "__main__":
    main()

    # 显示常量
    print(f"\n最大连接数: {MAX_CONNECTIONS}")
    print(f"默认超时时间: {default_timeout} 秒")


