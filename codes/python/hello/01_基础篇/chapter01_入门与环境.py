# 1.3 注释的写法
# =============================================================================

# 这是单行注释（以 # 开头，类似 C++ 的 // 注释）
# Python 没有 /* */ 这种多行注释语法

# 多行注释通常用三引号字符串（但它实际上是字符串字面量，只是被忽略了）
"""
这是一段
多行注释（实际上是字符串字面量）
在函数、类、模块开头使用时，它是"文档字符串"（docstring）
"""

# 文档字符串（docstring）的正式用法示例：
# （现在先看格式，第 6 章函数部分会详细讲解）
def greet(name: str) -> str:
    """
    向指定的人打招呼。

    Args:
        name: 人名字符串

    Returns:
        打招呼的字符串
    """
    return f"你好，{name}！"  # f-string 格式化（第 4 章详解）


