# =============================================================================
# 1.6 PEP 8 代码规范简介
# =============================================================================
#
# PEP 8 是 Python 官方的代码风格指南，类似于 C/C++ 中的代码规范。
# 良好的代码规范让代码更易读、维护。以下是最重要的几条：
#
# ── 命名规范 ────────────────────────────────────────────────────────────────
#
# 变量名和函数名：小写字母 + 下划线（snake_case）
#   user_name = "Alice"          ✅ 正确
#   userName = "Alice"           ❌ 不推荐（这是 camelCase，Java/C# 风格）
#   UserName = "Alice"           ❌ 不推荐（这是类名的写法）
#
# 常量：全大写 + 下划线
#   MAX_RETRY_COUNT = 3          ✅ 正确
#   PI = 3.14159                 ✅ 正确
#
# 类名：每个单词首字母大写（PascalCase / UpperCamelCase）
#   class UserManager:           ✅ 正确
#   class user_manager:          ❌ 不推荐
#
# 私有成员（类内部使用）：以下划线开头
#   _internal_variable = 42     # 单下划线：约定俗成的"内部使用"
#   __private_variable = 42     # 双下划线：Python 会进行名称改写（第 9 章详解）
#
# ── 缩进 ─────────────────────────────────────────────────────────────────────
#
# Python 用缩进来划定代码块（相当于 C/C++ 的大括号）
# 规范：使用 4 个空格（不要用 Tab）
#
# ── 行长度 ───────────────────────────────────────────────────────────────────
#
# 每行代码不超过 79 个字符（现代项目通常放宽到 100-120 字符）
#
# ── 空行 ─────────────────────────────────────────────────────────────────────
#
# 顶级函数和类定义之间：2 个空行
# 类内部方法之间：1 个空行
#
# ── 空格 ─────────────────────────────────────────────────────────────────────
#
# 运算符两侧：x = 1 + 2       ✅
#            x=1+2             ❌
# 函数参数默认值：def func(x=1) ✅ （默认参数的等号两边不加空格）
#
# ── 导入规范 ─────────────────────────────────────────────────────────────────
#
# 每行只导入一个模块
# import os                     ✅
# import sys                    ✅
# import os, sys                ❌（不推荐）
#
# 导入顺序（用空行分隔）：
#   1. 标准库
#   2. 第三方库
#   3. 本地模块

# 下面是规范命名的示例：

MAX_CONNECTIONS = 100           # 常量：全大写
default_timeout = 30            # 变量：小写 + 下划线

def calculate_circle_area(radius: float) -> float:  # 函数名：小写 + 下划线
    """计算圆的面积。"""
    PI = 3.14159265358979       # 函数内部的常量
    return PI * radius ** 2     # ** 是幂运算符（相当于 C 的 pow 函数）


class UserProfile:              # 类名：PascalCase
    """用户个人资料类。"""

    def __init__(self, name: str, age: int) -> None:
        self.name = name        # 公有属性
        self.age = age          # 公有属性
        self._score = 0         # 约定私有属性（单下划线）

    def get_info(self) -> str:  # 方法名：小写 + 下划线
        """返回用户信息字符串。"""
        return f"姓名: {self.name}, 年龄: {self.age}"


