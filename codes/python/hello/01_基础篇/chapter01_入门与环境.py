# 8. if __name__ == "__main__": - 程序入口判断
#
# ── 关键概念 ──
# - Python 用缩进（4个空格）代替 C/C++ 的 {} 表示代码块
# - Python 语句结尾不需要分号 ;（虽然加了也不报错，但不规范）
# - Python 是解释型语言，无需编译即可运行
# - Python 是动态类型语言，变量不需要声明类型
#
# ── 与 C/C++ 对比速记 ──
# C/C++: int x = 5;    → Python: x = 5
# C/C++: printf("Hello\n");  → Python: print("Hello")
# C/C++: // 注释       → Python: # 注释
# C/C++: { ... }       → Python: 缩进
# C/C++: int main(){}  → Python: if __name__ == "__main__":


