def main() -> None:
    """主函数：演示本章所有知识点。"""

    print("\n" + "=" * 50)
    print("第 1 章综合演示")
    print("=" * 50)

    # 1. 调用自定义函数
    message = greet("Python 学习者")
    print(message)

    # 2. 计算圆的面积
    area = calculate_circle_area(5.0)
    print(f"半径为 5 的圆面积: {area:.4f}")  # :.4f 表示保留 4 位小数

    # 3. 创建对象并使用
    user = UserProfile("张三", 28)
    print(user.get_info())

    # 4. 展示 Python 的简洁性
    print("\n── Python 的简洁性演示 ──")

    # 在 C/C++ 中交换两个变量需要临时变量：
    #   int temp = a; a = b; b = temp;
    # 在 Python 中：
    a = 10
    b = 20
    a, b = b, a  # 一行完成交换（元组解包，第 5 章详解）
    print(f"交换后：a = {a}, b = {b}")

    # 在 C/C++ 中生成 0-9 的列表：
    #   int arr[10]; for(int i=0; i<10; i++) arr[i] = i;
    # 在 Python 中：
    numbers = list(range(10))  # range 生成序列，list 转换为列表
    print(f"0 到 9 的列表: {numbers}")

    # 求列表中所有数的平方（列表推导式，第 5 章详解）
    squares = [x ** 2 for x in numbers]
    print(f"它们的平方: {squares}")


