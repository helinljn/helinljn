# 注意：Python 中 print 是一个函数，需要用括号调用（这是 Python 3 的规则）

print("Hello, World!")
print("你好，世界！")      # Python 原生支持 Unicode，中文无需特殊处理

# print 函数还支持很多参数：
print("第一行", "第二段", "第三段")          # 多个参数用空格连接
print("A", "B", "C", sep="-")               # sep 参数：指定分隔符
print("第一行", end="")                      # end 参数：指定结尾字符（默认是换行 \n）
print(" <-- 这行紧跟在前一行后面")           # 因为上面 end="" 所以没有换行


